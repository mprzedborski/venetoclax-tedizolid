function Make_expt_timing_plots

%This code makes the plot of the results from timing experiments. 

%Below are the experimental measurements of cellular viability on day 3 
%after administration of ABT-199.

%Average is first column, standard deviation is second column:

%ABT-199 monotherapy, no pre-treatment:
ABT_only = [1, 0.045335519;...
            0.478059357, 0.020052414;...
            0.398232845, 0.026885206;...
            0.39155582, 0.013438965;...
            0.341420627, 0.059578559;...
            0.319306764, 0.026595824;...
            0.324062672, 0.047290188;...
            0.26181485, 0.028373595;...
            0.209332685, 0.075717423;...
            0.013679644, 0.003123583;...
            0.00203538, 0.000449975;...
            0.000932902, 0.000141759];
	
%ABT-199 and Tedizolid combination therapy, no pre-treatment.
Comb = [1, 0.14269313;...
        0.414717699, 0.014650404;...
        0.329274379, 0.038823167;...
        0.214703461, 0.016572002;...
        0.167574925, 0.018310425;...
        0.104677588, 0.006125822;...
        0.079797738, 0.005979828;...
        0.048426561, 0.011432816;...
        0.017968805, 0.003229345;...
        0.005974582, 0.000766166;...
        0.002585079, 0.000845483;...
        0.001242996, 0.00031519];
	
%ABT-199 and Tedizolid combination therapy, tedizolid pre-treatment 48
%hours.
Pretreat = [1, 0.298289125;...
            0.220669324, 0.062087739;...
            0.11821522, 0.014850714;...
            0.107306474, 0.024371008;...
            0.059380312, 0.012796943;...
            0.080538977, 0.030391526;...
            0.040789798, 0.01639333;...
            0.036137204, 0.015345826;...
            0.059329761, 0.032553422;...
            0.040980899, 0.013149425;...
            0.031855985, 0.003068694;...
            0.013154408, 0.005410768];


%Make a plot of the cell viability results:
%In nM:
ABT_dose = [0, 9.765625, 19.53125, 39.0625, 78.125, 156.25, 312.5, 625,...
            1250, 2500, 5000, 10000];

%Nonlinear fits from Graphpad prism:
x = linspace(9.765625,10000,1000);   
ABT_fit = -0.1033 + (x.^(-0.8648))*(0.4281+0.1033)./(x.^(-0.8648) + 1218^(-0.8648));
Comb_fit = -0.01526 + (x.^(-0.5862))*(1.088+0.01526)./(x.^(-0.5862) + 4.636^(-0.5862));
Pretreat_fit = 0.0343 + (x.^(-0.7694))*(87.47-0.0343)./(x.^(-0.7694) + 0.003124^(-0.7694));

figure;
hold on;
errorbar(ABT_dose, ABT_only(:,1), ABT_only(:,2),'ko', 'LineWidth',3, 'MarkerSize',10,...
    'MarkerEdgeColor','k',...
    'MarkerFaceColor','k');
errorbar(ABT_dose, Comb(:,1), Comb(:,2),'bo', 'LineWidth',3, 'MarkerSize',10,...
    'MarkerEdgeColor','b',...
    'MarkerFaceColor','b');
errorbar(ABT_dose, Pretreat(:,1), Pretreat(:,2),'ro', 'LineWidth',3, 'MarkerSize',10,...
    'MarkerEdgeColor','r',...
    'MarkerFaceColor','r');
plot(x, ABT_fit, 'k-', 'LineWidth', 3);
plot(x, Comb_fit, 'b-', 'LineWidth', 3);
plot(x, Pretreat_fit, 'r-', 'LineWidth', 3);
set(gca,'fontsize',36);
xlabel('Venetoclax dose (nM)');
ylabel('Cell viability');
legend('ABT-199 monotherapy','Combination, no pre-treat', 'Tedizolid pre-treat');
%hold off;       
        
end